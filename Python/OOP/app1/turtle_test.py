import turtle

myturtle = turtle.Turtle()

myturtle.penup() # lift the pen up
myturtle.goto(50, 75) # move to x=50, y=75
myturtle.pendown() # put the pen down

myturtle.forward(100) # move forward 100 pixels
myturtle.left(90) # turn left 90 degrees
myturtle.forward(200) # move forward 200 pixels
myturtle.left(90) # turn left 90 degrees
